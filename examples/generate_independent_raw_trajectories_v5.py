def build_records() -> list[dict]:
    records = []

    for trajectory in TRAJECTORIES:
        trajectory_id = trajectory["trajectory_id"]

        for event in trajectory["events"]:
            record = {
                "trajectory_id": trajectory_id,
                "step": event["step"],
                "signature": event["signature"],
                "cluster": event["cluster"],
                "delta": event["delta"],
                "iri": event["iri"],
                "boundary_distance": event["boundary_distance"],
                "phase": event["phase"],
                "source": SOURCE,
                "source_independence": SOURCE_INDEPENDENCE,
                "independence_method": INDEPENDENCE_METHOD,
            }

            records.append(record)

    return records


def main() -> None:
    DATA_DIR.mkdir(exist_ok=True)

    records = build_records()

    OUTPUT_PATH.write_text(
        "\n".join(json.dumps(record, sort_keys=True) for record in records) + "\n",
        encoding="utf-8",
    )

    trajectory_ids = sorted({record["trajectory_id"] for record in records})

    print("=" * 100)
    print("GENERATE INDEPENDENT RAW TRAJECTORIES — v5")
    print("=" * 100)
    print()
    print(f"Output file: {OUTPUT_PATH.relative_to(ROOT)}")
    print(f"Source: {SOURCE}")
    print(f"Source independence: {SOURCE_INDEPENDENCE}")
    print(f"Independence method: {INDEPENDENCE_METHOD}")
    print(f"Trajectory count: {len(trajectory_ids)}")
    print(f"Event count: {len(records)}")
    print()
    print("Trajectories:")
    for trajectory_id in trajectory_ids:
        print(f"- {trajectory_id}")
    print()
    print("=" * 100)


if __name__ == "__main__":
    main()